package pojos;

public class Home {

    private String storeNumber;
    private String profileInformation;
    private String OtherstoreNumber;

    public String getStoreNumber() {
        return storeNumber;
    }

    public void setStoreNumber(String storeNumber) {
        this.storeNumber = storeNumber;
    }

    public String getProfileInformation() {
        return profileInformation;
    }

    public void setProfileInformation(String profileInformation) {
        this.profileInformation = profileInformation;
    }

    public String getOtherstoreNumber() {
        return OtherstoreNumber;
    }

    public void setOtherstoreNumber(String OtherstoreNumber) {
        this.OtherstoreNumber = OtherstoreNumber;
    }

}
